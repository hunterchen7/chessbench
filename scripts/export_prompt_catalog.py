"""Export exact, code-generated Standard prompt examples for the web UI.

The committed artifact is intentionally generated by the same builders used by
benchmark runs.  ``tests/test_prompt_catalog.py`` fails if it ever drifts.
"""

from __future__ import annotations

import json
from dataclasses import replace
from pathlib import Path

import chess

from chessbench.conditions import PUZZLE_SYSTEM_PROMPT, build_puzzle_prompt, mode_condition
from chessbench.response_protocols import ResponseProtocol, response_format_for


ROOT = Path(__file__).resolve().parents[1]
CORPUS_PATH = ROOT / "web/public/data/corpora/standard.json"
OUTPUT_PATH = ROOT / "web/public/data/prompts.json"

METHODS = (
    (1, 1, "Raw"),
    (2, 2, "Assisted"),
    (3, 3, "Coached"),
    (5, 4, "Deep coached"),
)
RESPONSE_STYLES = (
    ("move_only", False, ResponseProtocol.JSON_SCHEMA_V1),
    ("json_rationale", True, ResponseProtocol.JSON_SCHEMA_V1),
)


def build_catalog() -> dict[str, object]:
    corpus = json.loads(CORPUS_PATH.read_text())
    reference = corpus["items"][0]
    board = chess.Board(reference["fen"])

    methods: list[dict[str, object]] = []
    for internal_mode, display_mode, name in METHODS:
        styles: list[dict[str, object]] = []
        for style, explain, protocol in RESPONSE_STYLES:
            condition = replace(
                mode_condition(internal_mode),
                explain=explain,
                response_protocol=protocol,
            )
            styles.append(
                {
                    "style": style,
                    "response_protocol": condition.to_dict()["response_protocol"],
                    "condition_slug": condition.slug(),
                    "system_prompt": PUZZLE_SYSTEM_PROMPT,
                    "user_prompt": build_puzzle_prompt(board, condition),
                    "provider_response_format": response_format_for(
                        condition.response_protocol, "move", explain=condition.explain
                    ),
                }
            )
        methods.append(
            {
                "internal_mode": internal_mode,
                "display_mode": display_mode,
                "name": name,
                "prompt_version": styles[0]["condition_slug"].split("__prompt-")[1].split("__")[0],
                "styles": styles,
            }
        )

    return {
        "schema": "chessbench.prompt_catalog.v1",
        "scope": "standard_puzzle_first_turn",
        "reference": {
            "suite": corpus["name"],
            "content_hash": corpus["content_hash"],
            "puzzle_id": reference["puzzle_id"],
            "fen": reference["fen"],
        },
        "methods": methods,
    }


def main() -> None:
    OUTPUT_PATH.write_text(json.dumps(build_catalog(), indent=2) + "\n")
    print(f"wrote {OUTPUT_PATH.relative_to(ROOT)}")


if __name__ == "__main__":
    main()
